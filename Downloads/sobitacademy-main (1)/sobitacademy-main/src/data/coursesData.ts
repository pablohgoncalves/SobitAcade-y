export const getAllLessons = (): Lesson[] => {
  return categories.flatMap((cat) => cat.lessons);
};

export const getLessonById = (id: string): Lesson | undefined => {
  return getAllLessons().find((lesson) => lesson.id === id);
};

export const getCategoryById = (id: string): Category | undefined => {
  return categories.find((cat) => cat.id === id);
};

export const getNextLesson = (currentId: string): Lesson | undefined => {
  const allLessons = getAllLessons();
  const currentIndex = allLessons.findIndex((l) => l.id === currentId);
  return currentIndex >= 0 && currentIndex < allLessons.length - 1
    ? allLessons[currentIndex + 1]
    : undefined;
};

export const getPreviousLesson = (currentId: string): Lesson | undefined => {
  const allLessons = getAllLessons();
  const currentIndex = allLessons.findIndex((l) => l.id === currentId);
  return currentIndex > 0 ? allLessons[currentIndex - 1] : undefined;
};
